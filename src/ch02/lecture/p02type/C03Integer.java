package ch02.lecture.p02type;

public class C03Integer {
    public static void main(String[] args) {
        // 정수 표현 방법
        // 10진법
        int a = 32;
        int b = 23_333_222;

        // 2진법
        int c = 0b01001101;
        // 8진법
        int d = 027123;
        // 16진법
        int e = 0x82ace2;

        int var1 = 0b1011;
        int var2 = 0206;
        int var3 = 365;
        int var4 = 0xB3;

        System.out.println("var1:" + var1);
        System.out.println("var2:" + var2);
        System.out.println("var3:" + var3);
        System.out.println("var4:" + var4);


    }

}
